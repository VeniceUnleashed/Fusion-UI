const toStandardOrNumpadKey = (e, standardKey, numpadKey) => {
    if (e.location === KeyboardEvent.DOM_KEY_LOCATION_STANDARD)
        return standardKey;
    else if (e.location === KeyboardEvent.DOM_KEY_LOCATION_NUMPAD)
        return numpadKey;
}

